package com.yash;

public class ParentCalculator {

    public int AddOperation(int x, int y){
        return x+y;
    }
    public int SubOperation(int x, int y){
        return x-y;
    }
    public int MultiOperation(int x, int y){
        return x*y;
    }
    public int DivOperation(int x, int y){
        return x/y;
    }
}
